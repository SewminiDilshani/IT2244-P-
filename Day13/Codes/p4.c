//Print the integers from 1 to 10 using while loop.

#include<stdio.h>

#include <stdio.h>

int main() {
    int i = 1;  // Initialize the counter variable

    // Loop from 1 to 10 using while loop
    while(i <= 10) {
        printf("%d\n", i);  // Print the current value of i
        i++;                // Increment i by 1
    }

    return 0;  // End of the program
}
